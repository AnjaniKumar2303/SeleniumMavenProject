package org.example.java;

public class TypeConversion {

    public static void main(String[] args) {

        //String to int
        String s1 = "Test";
        int i1 = Integer.parseInt(s1);
        System.out.println(i1);

        //String to integer
        String s2 = "Test";
        Integer i2 = Integer.parseInt(s2);
        System.out.println(i2);
    }
}
